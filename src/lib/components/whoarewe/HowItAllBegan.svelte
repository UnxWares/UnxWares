<h4>VFF-Project : un projet frère</h4>
<p>
	En même temps, on lance <strong>VFF Project</strong> : parce qu'on n'est pas juste une boîte de cloud, on est
	aussi des gens qui aiment documenter le monde réel. VFF est une <em>encyclopédie ferroviaire française</em>,
	communautaire, qui veut recenser <strong>les lignes, les gares, les archives, les photos, les plans</strong>,
	avec une précision au mètre, en s'adossant à <strong>OpenStreetMap</strong>. Ce n'est pas un petit side-project :
	c'est un exemple concret de ce que doit être la communauté UnxWares.
</p>

<h4>SummumNetwork : les racines techniques</h4>
<p>
	Même si on ne veut pas que notre histoire commence par SummumNetwork, il faut en parler car c'est là qu'on a
	forgé notre expertise technique :
</p>
<ul>
	<li>
		<p>
			<strong>Projet Minecraft 100% custom</strong> : développé entièrement sur Forge avec des APIs maison
		</p>
	</li>
	<li>
		<p>
			<strong>Features jamais vues</strong> : on a poussé Minecraft Java à ses limites techniques
		</p>
	</li>
	<li>
		<p>
			<strong>Première confrontation aux hébergeurs</strong> : c'est là qu'on a compris que le marché
			de l'hébergement avait de gros problèmes
		</p>
	</li>
	<li>
		<p>
			<strong>Arrêt en février 2024</strong> : on a estimé que Minecraft Java n'avait pas grand avenir et
			que l'écosystème partait dans une mauvaise direction
		</p>
	</li>
</ul>
<p>
	C'est cette expérience qui nous a donné l'envie de créer <strong>UnxWares Studio</strong> et de développer
	notre propre jeu sous <strong>Unreal Engine 5</strong>, plutôt que de rester dépendants de l'écosystème Minecraft.
</p>
