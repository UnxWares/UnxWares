<h3>1. Comment tout a commencé</h3>
<p>
	Même si le projet <strong>SummumNetwork</strong> (10/02/2022 - 10/02/2024) est dans l'ADN — parce qu'on y a appris
	ce que c'est que de tout développer soi-même, d'intégrer des APIs Minecraft custom, et de pousser
	des features jamais vues — <strong>on ne veut pas que l'histoire d'UnxWares commence avec ça.</strong>
</p>

<h4>La prise de conscience</h4>
<p><strong>L'histoire d'UnxWares commence vraiment en 2023</strong>, quand on s'est dit :</p>
<blockquote>
	<p>
		"On ne veut plus dépendre d'hébergeurs qui font "comme les autres" et qui ont des pratiques qui
		ne nous conviennent pas.<br/>
		On veut notre propre infra, notre propre façon d'orchestrer, notre façon de faire du cloud, et
		on veut la partager.<br/>
		On sait qu'on a des compétences, qu'on a acquis en ne voulant pas faire comme tout le monde,
		servons-nous en pour changer les choses et concevoir une autre façon d'héberger et de développer."
	</p>
</blockquote>

