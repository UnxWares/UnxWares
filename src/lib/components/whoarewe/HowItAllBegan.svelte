<h3>1. Comment tout a commencé</h3>
<p>
	Même si le projet SummumNetwork (10/02/2022-10/02/2024) est dans l’ADN — parce qu’on y a appris
	ce que c’est que de tout développer soi-même, d’intégrer des APIs Minecraft custom, et de pousser
	des features jamais vues — on ne veut pas que l’histoire d’UnxWares commence avec ça.
</p>
<p><strong>
	L’histoire d’UnxWares commence vraiment en 2023, quand on s’est dit :
	</strong>
	<cite>
		On ne veut plus dépendre d’hébergeurs qui font “comme les autres” et qui ont des pratiques qui
		ne nous conviennent pas.
		On veut notre propre infra, notre propre façon d’orchestrer, notre façon de faire du cloud, et
		on veut la partager.
		On sait qu'on a des compétences, qu'on a acquis en ne voulant pas faire comme tout le monde,
		servons-nous en pour changer les choses et concevoir une autre façon d'héberger et de développer.
	</cite>
</p>
<p>
	À ce moment-là, on tourne sur un Ryzen 7 3700X, on vient même d’un setup où un i5-8500 a
	littéralement rendu l’âme, et malgré ça on avait déjà un truc très clair : on ira vers du matériel
	pro, vers une infra hébergée en France, vers du Kubernetes/Proxmox, mais sans tomber dans le
	modèle “on fait payer parce qu’on peut”.
</p>
<span><strong>C’est là qu’UnxWares naît : le 19 octobre 2023.</strong></span>
<p>
	En même temps, on lance VFF Project : parce qu’on n’est pas juste une boîte de cloud, on est
	aussi des gens qui aiment documenter le monde réel. VFF est une encyclopédie ferroviaire française,
	communautaire, qui veut recenser les lignes, les gares, les archives, les photos, les plans,
	avec une précision au mètre, en s’adossant à OpenStreetMap. Ce n’est pas un petit side-project :
	c’est un exemple concret de ce que doit être la communauté UnxWares.
</p>