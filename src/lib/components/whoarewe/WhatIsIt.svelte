<h3>UnxWares, c’est quoi ?</h3>
<p>
	UnxWares SAS (immatriculée le 03 juin 2025 au RCS de Paris) est une entreprise française de
	développement et d’hébergement informatique née d’un truc très simple : deux techos qui en
	avaient marre de dépendre d’infrastructures opaques, chères, parfois incompétentes, et qui
	voulaient tout faire eux-mêmes.
</p>
