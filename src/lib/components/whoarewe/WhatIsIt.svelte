<h3>UnxWares, c'est quoi ?</h3>
<p>
	UnxWares SAS (immatriculée le <strong>03 juin 2025</strong> au RCS de Paris) est une entreprise française de
	développement et d'hébergement informatique née d'un truc très simple : <strong>deux techos qui en
	avaient marre de dépendre d'infrastructures opaques, chères, parfois incompétentes, et qui
	voulaient tout faire eux-mêmes.</strong>
</p>

