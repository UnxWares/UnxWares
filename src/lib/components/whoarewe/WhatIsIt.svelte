<p>
	Ce projet, c'est tout sauf une idée sortie d'un incubateur, tout sauf un projet "start-up" sur
	PowerPoint. <strong>C'est né parce qu'on en avait marre.</strong> Marre des hébergeurs qui vendent du rêve,
	marre de la nonchalance technique, marre des prix absurdes "parce que tout le monde fait pareil".
	Et ça change tout : <strong>UnxWares n'est pas une boîte fondée par des commerciaux qui ont ensuite cherché
	des développeurs.</strong> C'est l'inverse. On est partis de la technique, puis on a mis un cadre autour.
</p>

<h4>Notre objectif</h4>
<p>
	Proposer une <strong>alternative française, souveraine, honnête, très technique, mais
	accessible</strong>, dans la continuité de ce que fait par exemple Infomaniak… mais en poussant encore
	plus loin l'idée de "tout maîtriser soi-même", y compris l'orchestrateur, le réseau, et des
	briques métiers.
</p>

