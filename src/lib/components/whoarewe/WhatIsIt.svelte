<h4>Notre positionnement</h4>
<p>
	Notre positionnement tient en une phrase :
</p>
<blockquote>
	<p><strong>On veut faire du cloud et du logiciel comme on aurait aimé que d'autres le fassent avant nous.</strong></p>
</blockquote>
<p>
	On est dans la mouvance éthique d'<strong>Infomaniak</strong>, poussée à son maximum. Car de base, nous les fondateurs,
	on est des techies, on est passionnés par la tech, donc on n'a pas un regard de businessman.
	On veut construire quelque chose de bien, pas juste quelque chose de rentable.
</p>
