<p><strong>Nous, ça ne nous allait pas.</strong></p>

<h4>Ce qu'on voulait vraiment</h4>
<p><strong>Nous, on voulait :</strong></p>
<ul>
	<li>
		<p>
			être <strong>réellement souverains</strong> (infra en France, à Caen, réseau fibré, stockage maîtrisé)
		</p>
	</li>
	<li>
		<p>
			être <strong>transparents</strong> (dire sur quoi on tourne, montrer les gammes de serveurs, parler du
			firewall Fortinet, des Mikrotiks, des SLA, pas faire de la magie)
		</p>
	</li>
	<li>
		<p>
			être <strong>accessibles aux développeurs</strong> (vous pouvez causer Go, Rust, Laravel, SvelteKit avec
			nous, on saura vous répondre, du moins on aura les connaissances pour vous comprendre)
		</p>
	</li>
	<li>
		<p>
			proposer un <strong>nouveau modèle de travail plus éthique et vertueux</strong>, et qui permet à tout le
			monde d'être gagnant. Moins de complexité pour les clients, avec un seul interlocuteur,
			moins de blabla pour les développeurs qui peuvent faire ce qu'ils aiment
		</p>
	</li>
	<li>
		<p>
			et surtout <strong>ne pas jouer à l'illusion commerciale</strong> ("on fait pareil que les GAFAM mais avec
			un dashboard violet")
		</p>
	</li>
</ul>

<h4>Notre positionnement</h4>
<p>Notre positionnement tient en une phrase :</p>
<blockquote>
	<p><strong>On veut faire du cloud et du logiciel comme on aurait aimé que d'autres le fassent avant nous.</strong></p>
</blockquote>

