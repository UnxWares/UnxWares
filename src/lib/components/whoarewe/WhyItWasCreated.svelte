<h3>2. Pourquoi on a créé UnxWares</h3>
	<h4>On l’a créé parce qu’on a constaté un truc qui revient sans cesse :</h4>
	<ol>
		<li>
			<p>
				Les hébergeurs grand public sont trop simplistes : ça marche tant que tu veux un WordPress
				ou un petit VPS. Pour plus gros, il faut un sacré budget !
			</p>
		</li>
		<li>
			<p>
				Les hébergeurs pros sont souvent trop chers ou trop fermés : “on ne vous explique pas trop
				ce qu’on fait, mais c’est 4× plus cher”.
			</p>
		</li>
		<li>
			<p>
				Beaucoup surfent sur le marché sans réelle valeur technique : même infra que le voisin,
				mais sur-emballée.
			</p>
		</li>
		<li>
			<p>
				La souveraineté est souvent un argument marketing : on met “France” sur la page, mais
				derrière c’est mal documenté.
			</p>
		</li>
	</ol>
	<span><strong>Nous, ça ne nous allait pas.</strong></span>
	<h4>Nous, on voulait :</h4>
	<ul>
		<li>
			<p>
				être réellement souverains (infra en France, à Caen, réseau fibré, stockage maîtrisé)
			</p>
		</li>
		<li>
			<p>
				être transparents (dire sur quoi on tourne, montrer les gammes de serveurs, parler du
				firewall Fortinet, des Mikrotiks, des SLA, pas faire de la magie)
			</p>
		</li>
		<li>
			<p>
				être accessibles aux développeurs (vous pouvez causer Go, Rust, Laravel, SvelteKit avec
				nous, on saura vous répondre, du moins on aura les connaissances pour vous comprendre)
			</p>
		</li>
		<li>
			<p>
				proposer un nouveau modèle de travail plus éthique et vertueux, et qui permet à tout le
				monde d'être gagnant. Moins de complexité pour les clients, avec un seul interlocuteur,
				moins de blabla pour les développeurs qui peuvent faire ce qu'ils aiment
			</p>
		</li>
		<li>
			<p>
				et surtout ne pas jouer à l’illusion commerciale (“on fait pareil que les GAFAM mais avec
				un dashboard violet”).
			</p>
		</li>
	</ul>
<p>
	Notre positionnement tient en une phrase :
	<cite>
		On veut faire du cloud et du logiciel comme on aurait aimé que d’autres le fassent avant nous.
	</cite>
</p>